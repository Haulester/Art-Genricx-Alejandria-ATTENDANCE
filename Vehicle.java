public class Vehicle {
    // Method to indicate that the vehicle is running
    void run() {
        System.out.println("The vehicle is running");
    }

    // Method to stop the vehicle
    void stop() {
        System.out.println("Stop");
    }

    // Method to indicate the speed of the vehicle
    void Speed() {
        System.out.println("fast");
    }

    // Method to indicate the color of the vehicle
    void Color() {
        System.out.println("rainbow");
    }

    // Method to indicate the price of the vehicle
    void Price() {
        System.out.println("expensive");
    }

    // Method to indicate the model of the vehicle
    void model() {
        System.out.println("sikret");
    }
}